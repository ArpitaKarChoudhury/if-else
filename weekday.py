# 11. Write a python program to input week number and print week day.
day=int(input("enter day number"))
if day==1:
    print("Sunday")
elif 2==day:
    print("monday")
elif day==3:
    print("tuesday")
elif day==4:
    print("Wednesday")
elif day==5:
    print("Thursday")
elif day==6:
    print("Friday")
elif day==7:
    print("saturday")
else:
    print("incorrect the day number")